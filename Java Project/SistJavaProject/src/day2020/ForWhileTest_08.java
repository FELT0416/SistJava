package day2020;

public class ForWhileTest_08 {
    public static void main(String[] args) {
        for (int i = 10; i >= 0; i--) {


            System.out.println(i+" ");

        }
        System.out.println();

        int a = 10;
        while (a > 0) {
            System.out.println(a--+"  ");
        }
        System.out.println();

    }
}
