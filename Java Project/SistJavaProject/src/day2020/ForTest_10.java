package day2020;

public class ForTest_10 {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            System.out.printf("%d ",i);
        }
        System.out.println();
        for (int i = 10; i > 0; i--) {
            System.out.printf("%d ",i);
        }

    }
}
